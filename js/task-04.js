// Switch
const stars = 6;
switch (stars) {
  case 1:
    console.log("Hotel 1 star");
    break;
  case 2:
    console.log("Hotel 2 stars");
    break;
  case 3:
    console.log("Hotel 3 stars");
    break;
  case 4:
    console.log("Hotel 4 stars");
    break;
  case 5:
    console.log("Hotel 5 stars");
    break;
  default:
    console.log("Неверный ввод!");
}
